// Lookup by name (case-insensitive)
export function getCityByName(name: string): NCRBCityData | undefined {
  const lower = name.toLowerCase();
  return NCRB_CITIES.find(
    (c) => c.name.toLowerCase() === lower || c.name.toLowerCase().includes(lower)
  );
}

// Get nearest city to coordinates
export function getNearestCity(lat: number, lng: number): NCRBCityData {
  let nearest = NCRB_CITIES[0];
  let minDist = Infinity;
  for (const city of NCRB_CITIES) {
    const d = Math.sqrt(
      Math.pow(city.lat - lat, 2) + Math.pow(city.lng - lng, 2)
    );
    if (d < minDist) {
      minDist = d;
      nearest = city;
    }
  }
  return nearest;
}

// Top N safest cities
export function getSafestCities(n = 10): NCRBCityData[] {
  return [...NCRB_CITIES].sort((a, b) => b.safetyScore - a.safetyScore).slice(0, n);
}

// Top N most dangerous cities
export function getMostDangerousCities(n = 10): NCRBCityData[] {
  return [...NCRB_CITIES].sort((a, b) => a.safetyScore - b.safetyScore).slice(0, n);
}
